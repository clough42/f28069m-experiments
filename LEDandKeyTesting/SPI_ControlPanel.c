Uint16 lcd_char(Uint16 x)
{
    static const Uint16 table[] = {
        0b1111110000000000, // 0
        0b0110000000000000, // 1
        0b1101101000000000, // 2
        0b1111001000000000, // 3
        0b0110011000000000, // 4
        0b1011011000000000, // 5
        0b1011111000000000, // 6
        0b1110000000000000, // 7
        0b1111111000000000, // 8
        0b1111011000000000, // 9
        0b0000000100000000  // .
    };
    if( x < sizeof(table) ) {
        return table[x];
    }
    return table[sizeof(table)-1];
}

void SendByte(Uint16 data) {
    SpiaRegs.SPITXBUF = data;
    while(SpiaRegs.SPISTS.bit.INT_FLAG !=1) {}
    dummy = SpiaRegs.SPIRXBUF;
}

void SendControlPanelData(Uint16 data[], Uint16 ledMask)
{
    int i;
    SpiaRegs.SPICTL.bit.TALK = 1;

    GpioDataRegs.GPACLEAR.bit.GPIO19 = 1;       // lower the STB line
    SendByte(reverse_byte(0x40));
    GpioDataRegs.GPASET.bit.GPIO19 = 1;       // raise the STB line when done

    GpioDataRegs.GPACLEAR.bit.GPIO19 = 1;       // lower the STB line
    SendByte(reverse_byte(0xc0));
    for( i=0; i < 8; i++ ) {
        SendByte(lcd_char(data[i]));
        SendByte( (ledMask & 0x80) ? 0xff00 : 0x0000 );
        ledMask <<= 1;
    }
    GpioDataRegs.GPASET.bit.GPIO19 = 1;       // raise the STB line when done

    SpiaRegs.SPICTL.bit.TALK = 0;
}
